public class ArrayImplemenrtation {

	public static void main(String[] args) {
		//initialize the array
		int[] arr=new int[5];
		//value assigned
		arr[0]=1;
		arr[1]=2;
		arr[2]=3;
		arr[3]=4;
		arr[4]=5;
		
		//accessing the array
		for (int i=0;i<5;i++)
		{
			System.out.println("Element at index"+i+":"+arr[i]);
		}

	}

}

